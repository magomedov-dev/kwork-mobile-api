"""Генерация идентификатора установки `uad`.

В оригинальном приложении `uad` = SHA-1(build-props + android_id + time), в виде
40-символьного hex в верхнем регистре, создаётся один раз и кэшируется. Нам важно
лишь иметь стабильный псевдослучайный идентификатор того же формата — генерируем
случайный и переиспользуем (через хранилище сессии).
"""

from __future__ import annotations

import hashlib
import os


def generate_uad() -> str:
    """Сгенерировать новый `uad`: 40-символьный hex в верхнем регистре (как SHA-1)."""
    return hashlib.sha1(os.urandom(32)).hexdigest().upper()
